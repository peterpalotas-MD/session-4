import { useState, useEffect } from 'react'
import './App.css'

function App() {
  const [data, setData] = useState(null);

  useEffect(() => {
    //got the below from the curl request at https://open-meteo.com/
    //longitude and lat ofl ondon is 51.5072° N, 0.1276° W
    fetch("https://api.open-meteo.com/v1/forecast?latitude=51.50&longitude=0.12&current=temperature_2m,wind_speed_10m&hourly=temperature_2m,relative_humidity_2m,wind_speed_10m"
    )
        .then(res => res.json())
        .then(json => setData(json));
  }, []);



  return (
      <div>
        <h1>My very amazing and cool weather app</h1>
        {console.log(data)}
        <h1>London:</h1>
        <h2>Temp: {data?.current?.temperature_2m}°C</h2>
        <h2>Wind speed: {data?.current?.wind_speed_10m}km/h</h2>
        <h2>Time: {data?.current?.time}</h2>
      </div>
  );
}

export default App
